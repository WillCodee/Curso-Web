/*
OPERADORES DE COMPARAÇÃO
 > Maior que
 >= Maior que ou igual a
 < menor que
 <= menor que ou igual a
 == igualdade (valor) ***************
 === igualdade estrita (valor e tipo)
 != diferente (valor) ***************
 !== diferente estrito (valor e tipo)
*/

console.log(10>5) //10 é maior que 5
console.log(6<4) // 6 é menor q 4
console.log(10 >= 5) //É Maior OU Igual a 5
console.log(6<=4) //É Maior OU igual 4

const num1 = 10; //number
const num2 = '10'; //string
console.log(num1 == num2) //num1 é Igual a num2 ?    
console.log(num1 === num2) //num1 é igual (em valor e tipo) a num2 ?
console.log(num1 != num2) //num1 é Diferente a num2 ?
console.log(num1 !== num2) //num1 é Diferente (em valor e tipo) a num2 ?








